# ─────────────────────────────────────────────────────────────────────────
# Expansion / serialization
# ─────────────────────────────────────────────────────────────────────────
USER_PARAPHRASES = {
    "prefix": ["", "嗨, ", "刘老师, ", "请问 ", "想问下, ", "诶, ", "其实想问 "],
    "suffix": ["", " 啊", " 呢", "?", "...", " 谢谢"],
}


def expand_one(user: str, assistant: str, n: int = 3) -> list[tuple[str, str]]:
    out = [(user, assistant)]
    used = {user}
    tries = 0
    while len(out) < n and tries < 20:
        tries += 1
        prefix = random.choice(USER_PARAPHRASES["prefix"])
        suffix = random.choice(USER_PARAPHRASES["suffix"])
        variant = f"{prefix}{user.rstrip('?.!。！？')}{suffix}".strip()
        if variant and variant not in used:
            used.add(variant)
            out.append((variant, assistant))
    return out


def single_turn_records(user: str, assistant: str) -> list[dict]:
    return [
        {
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": u},
                {"role": "assistant", "content": a},
            ]
        }
        for u, a in expand_one(user, assistant, n=3)
    ]


def multi_turn_records(conv: list[tuple[str, str]]) -> list[dict]:
    """Split a multi-turn dialogue into N records, each ending at a
    different assistant turn — so every assistant turn contributes loss
    under train_lora.py's last-assistant-only masking."""
    msgs = [{"role": "system", "content": SYSTEM_PROMPT}]
    for role, content in conv:
        msgs.append({"role": role, "content": content})

    out = []
    for i in range(len(msgs)):
        if msgs[i]["role"] == "assistant":
            out.append({"messages": msgs[: i + 1]})
    return out


def main() -> None:
    records: list[dict] = []

    for user, assistant in SEEDS:
        records.extend(single_turn_records(user, assistant))

    multi_record_count = 0
    for conv in MULTI_TURN_SEEDS:
        new = multi_turn_records(conv)
        records.extend(new)
        multi_record_count += len(new)

    random.shuffle(records)
    n_eval = max(20, len(records) // 20)
    eval_split = records[:n_eval]
    train_split = records[n_eval:]

    train_path = OUT_DIR / "zhiyuan_train.jsonl"
    eval_path = OUT_DIR / "zhiyuan_eval.jsonl"
    with train_path.open("w", encoding="utf-8") as f:
        for rec in train_split:
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")
    with eval_path.open("w", encoding="utf-8") as f:
        for rec in eval_split:
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")

    print(f"single-turn seeds: {len(SEEDS)}")
    print(f"multi-turn dialogues: {len(MULTI_TURN_SEEDS)} → {multi_record_count} records")
    print(f"after expansion: {len(records)} ({len(train_split)} train + {len(eval_split)} eval)")
    print(f"train → {train_path}")
    print(f"eval  → {eval_path}")


if __name__ == "__main__":
    main()
